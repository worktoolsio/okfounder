/**
 * Returns a base 62 ID with n characters
 *
 * by default, 7 characters for 3521614606208 combinaitions
 *
 * @param n
 */
export const base62Id = (n = 7): string => {
  let result = '';
  for (let index = 0; index < n; index++) {
    result += base62chars[Math.round(Math.random() * (base62chars.length - 1))];
  }
  return result;
};
